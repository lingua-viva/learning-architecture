"""Layer 5 Gauntlet: Contamination Stress Test — 10 students × 50 observations, zero leakage.

Property proved: At scale, the system maintains perfect student isolation.
"""

import tempfile
from pathlib import Path

import pytest

from src.education.student_lens import StudentLensStore, Observation

N_STUDENTS = 10
N_OBS_PER_STUDENT = 50


def _build_store(db_path: Path) -> tuple[StudentLensStore, dict[str, list[str]]]:
    """Create N_STUDENTS students, each with N_OBS_PER_STUDENT observations.

    Returns the store and a map of student_id -> list of that student's raw
    transcripts (each transcript is unique per (student, observation index),
    so it doubles as a per-observation identity marker for leakage checks).
    """
    store = StudentLensStore(db_path=db_path)
    transcripts_by_student: dict[str, list[str]] = {}
    for s in range(N_STUDENTS):
        sid = store.create_lens(display_name=f"Student {s}", grade_level="G3")
        transcripts = []
        for o in range(N_OBS_PER_STUDENT):
            transcript = f"obs marker student-{s} observation-{o}"
            store.append_observation(Observation(
                student_id=sid,
                teacher_id="teacher-stress",
                template_type="literacy",
                raw_transcript=transcript,
            ))
            transcripts.append(transcript)
        transcripts_by_student[sid] = transcripts
    return store, transcripts_by_student


def test_gauntlet_ten_students_created():
    """10 students with 50 observations each — all created without error."""
    with tempfile.TemporaryDirectory() as tmp:
        store, transcripts_by_student = _build_store(Path(tmp) / "stress.db")
        assert len(transcripts_by_student) == N_STUDENTS
        for sid, transcripts in transcripts_by_student.items():
            assert len(transcripts) == N_OBS_PER_STUDENT
        store.close()


def test_gauntlet_each_lens_exact_data():
    """Each lens contains exactly that student's observations (count matches)."""
    with tempfile.TemporaryDirectory() as tmp:
        store, transcripts_by_student = _build_store(Path(tmp) / "stress.db")
        for sid in transcripts_by_student:
            rows = store._conn.execute(
                "SELECT COUNT(*) AS n FROM observations WHERE student_id = ?", (sid,)
            ).fetchone()
            assert rows["n"] == N_OBS_PER_STUDENT
        store.close()


def test_gauntlet_no_observation_in_two_lenses():
    """No observation_id appears in more than one student's lens."""
    with tempfile.TemporaryDirectory() as tmp:
        store, transcripts_by_student = _build_store(Path(tmp) / "stress.db")

        seen_ids: set[int] = set()
        for sid in transcripts_by_student:
            rows = store._conn.execute(
                "SELECT observation_id FROM observations WHERE student_id = ?", (sid,)
            ).fetchall()
            obs_ids = {r["observation_id"] for r in rows}
            assert obs_ids.isdisjoint(seen_ids), "observation_id leaked across students"
            seen_ids |= obs_ids

        assert len(seen_ids) == N_STUDENTS * N_OBS_PER_STUDENT
        store.close()


def test_gauntlet_remove_one_others_unchanged():
    """Removing one student's data doesn't affect other students' lenses."""
    with tempfile.TemporaryDirectory() as tmp:
        store, transcripts_by_student = _build_store(Path(tmp) / "stress.db")
        student_ids = list(transcripts_by_student.keys())
        target = student_ids[0]
        others = student_ids[1:]

        lenses_before = {sid: store.get_lens(sid) for sid in others}

        store.delete_lens(target)

        lenses_after = {sid: store.get_lens(sid) for sid in others}
        assert lenses_before == lenses_after
        store.close()


def test_gauntlet_rerun_identical():
    """Re-running the entire generation produces byte-identical lenses.

    "Byte-identical" is interpreted per-student-shape: rebuilding the same
    10x50 scenario from scratch produces lenses with identical structure
    and content for every student (deterministic recalculation from the
    append-only observation log), not literally identical DB files (which
    would differ by autogenerated student_id/timestamps across runs).
    """
    with tempfile.TemporaryDirectory() as tmp1, tempfile.TemporaryDirectory() as tmp2:
        store1, transcripts1 = _build_store(Path(tmp1) / "stress.db")
        store2, transcripts2 = _build_store(Path(tmp2) / "stress.db")

        lenses1 = sorted(
            (store1.get_lens(sid) for sid in transcripts1), key=lambda lens: lens["display_name"]
        )
        lenses2 = sorted(
            (store2.get_lens(sid) for sid in transcripts2), key=lambda lens: lens["display_name"]
        )

        def _strip_run_specific(lens: dict) -> dict:
            # Exclude fields that legitimately differ across two separate
            # runs (identity + wall-clock timestamps) — the property under
            # test is structural/content determinism, not that two runs
            # started at literally the same microsecond.
            out = {k: v for k, v in lens.items() if k not in ("student_id", "created_at", "updated_at")}
            out["rti_tier_history"] = [
                {k: v for k, v in entry.items() if k != "from" and k != "to"}
                for entry in out["rti_tier_history"]
            ]
            return out

        assert len(lenses1) == len(lenses2)
        for l1, l2 in zip(lenses1, lenses2):
            assert _strip_run_specific(l1) == _strip_run_specific(l2)
        store1.close()
        store2.close()
